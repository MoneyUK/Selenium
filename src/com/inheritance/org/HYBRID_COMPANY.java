package com.inheritance.org;

public class HYBRID_COMPANY {
	public void COMPANY() {
		System.out.println("COMPANY");
		System.out.println("HYUNDAI");
		System.out.println("MARAMALAINAGAR");
		System.out.println();
	}
	public void SUPERMARKET() {
		System.out.println("SUPERMARKET");
		System.out.println("NALINI STORES");
		System.out.println("TOYS SECTION");
		System.out.println( );
	}
public static void main(String[] args) {
	HYBRID_COMPANY obj = new HYBRID_COMPANY();
	obj.COMPANY();
	obj.SUPERMARKET();
}
}
