package com.inheritance.org;

public class INHERITANCE {

	public void supermarket() {
		System.out.println("SUPERMARKET");
		System.out.println("MANIKANDAN STROES");
		System.out.println("BOYS SECTION");
		System.out.println();

	}

	public void saravanastores() {
		System.out.println("SARAVANA STORES");
		System.out.println("PORUR");
		System.out.println("GENTS SECTION");
		System.out.println();
	}

	public static void main(String[] args) {
		INHERITANCE obj = new INHERITANCE();
		obj.supermarket();
		obj.saravanastores();

	}

}
