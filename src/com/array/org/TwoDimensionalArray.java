package com.array.org;

public class TwoDimensionalArray {
	public static void main(String[] args) {
		
		int a[][]= {{12,13,14,15,16,17},{23,24,25,26,27,28}};
		
		for (int i = 0; i < 2; i++) {
			
			for (int j = 0; j < 4; j++) {
				
			
			System.out.print(a[i][j]+"");
			
		}
		System.out.println( );
	}
     
}
}
